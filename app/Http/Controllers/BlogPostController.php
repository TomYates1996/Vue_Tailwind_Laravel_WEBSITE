    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newPost = new BlogPost;
        $newPost->title = $request->blog_post['title'];
        $newPost->content = $request->blog_post['content'];
        $newPost->author = $request->blog_post['author'];
        $newPost->imageurls = $request->blog_post['imageurls'];
        $newPost->save();

        return $newPost;
    }

